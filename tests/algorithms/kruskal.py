from algorithms.kruskal import *

def test_kruskal():
  assert kruskal({
    1: [(2, 4)],
    2: [(1, 4), (3, 1), (4, 1)],
    3: [(2, 1)],
    4: [(2, 1)]
  }) == {1: [2], 2: [3, 4, 1], 3: [2], 4: [2]}

def test_kruskal_complex():
  assert kruskal({
    1: [(2, 3), (5, 5)],
    2: [(1, 3), (5, 6), (3, 5)],
    3: [(2, 5), (6, 3), (4, 9)],
    4: [(3, 9), (6, 7)],
    5: [(1, 5), (2, 6), (6, 2)],
    6: [(5, 2), (3, 3), (4, 7)]
  }) == {1: [2, 5], 2: [1], 3: [6], 4: [6], 5: [6, 1], 6: [5, 3, 4]}